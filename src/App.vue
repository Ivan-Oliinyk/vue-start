<template>
  <div>
    <button type="button" @click="addLike">Likes</button>
    <button type="button" @click="addDislike">Dislike</button>
  </div>
  <div>Likes count: <strong>{{ likes }}</strong></div>
  <div>Dislikes count: <strong>{{ dislikes }}</strong></div>
</template>

<script>
export default {
  data() {
    return {
      likes: 0,
      dislikes: 0
    }
  },
  methods: {
    addLike() {
      this.likes += 1;
    },
    addDislike() {
      this.dislikes += 1;
    }
  }
}
</script>

